import { GetServerSideProps } from 'next'

import { ProductsService } from 'src/api'
import { Home, NextHead } from 'src/components'
import { withAuth } from 'src/hoc'
import { IPaginationProduct } from 'src/types'

const HomePage = ({ length, products }: IPaginationProduct) => {
  return (
    <>
      <NextHead title="Amazon" description="Generated by create next app" />
      <Home products={products} length={length} />
    </>
  )
}

export const getServerSideProps: GetServerSideProps<
  IPaginationProduct
> = async () => {
  const { data } = await ProductsService.getAll({
    page: 1,
    perPage: 8,
  })

  return {
    props: data,
  }
}

export default withAuth(HomePage)
