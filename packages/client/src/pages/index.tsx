import { ProductsService } from '@amazon/common/src'
import { GetStaticProps } from 'next'

import { Catalog, MainLayout, NextHead } from 'src/components'
import { withAuthPublic } from 'src/hoc'
import { IPaginationProduct } from 'src/types'

interface Props {
  products: IPaginationProduct
}

const HomePage = ({ products }: Props) => {
  return (
    <>
      <NextHead title="Amazon" description="Generated by create next app" />
      <MainLayout>
        <Catalog products={products} title="Freshed products" />
      </MainLayout>
    </>
  )
}

export const getStaticProps: GetStaticProps<Props> = async () => {
  const data = await ProductsService.getAll({
    page: 1,
    perPage: 10,
  })

  return {
    props: {
      products: data,
    },
    revalidate: 10,
  }
}

export default withAuthPublic(HomePage)
